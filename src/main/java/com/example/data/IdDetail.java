package com.example.data;

public class IdDetail {
    int type;
    String name;
    String issued_date;
    String jumin;
    String region;
    String driver_type;
    String driver_number;
    String driver_serial;

    public int getType() {
        return type;
    }
    public void setType(int type) {
        this.type = type;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getIssued_date() {
        return issued_date;
    }
    public void setIssued_date(String issued_date) {
        this.issued_date = issued_date;
    }
    public String getJumin() {
        return jumin;
    }
    public void setJumin(String jumin) {
        this.jumin = jumin;
    }
    public String getRegion() {
        return region;
    }
    public void setRegion(String region) {
        this.region = region;
    }
    public String getDriver_type() {
        return driver_type;
    }
    public void setDriver_type(String driver_type) {
        this.driver_type = driver_type;
    }
    public String getDriver_number() {
        return driver_number;
    }
    public void setDriver_number(String driver_number) {
        this.driver_number = driver_number;
    }
    public String getDriver_serial() {
        return driver_serial;
    }
    public void setDriver_serial(String driver_serial) {
        this.driver_serial = driver_serial;
    }
}

